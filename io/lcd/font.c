static bool omori_lookup(utf8_t c, font_symbol_t *sym, const font_table_t* font_table)
{
    const font_table_t *t = 0;

    // --- EDIT BEGIN
    if (c == '!') {
        t = &font_table[0];
    }
    else if (c == '#') {
    	t = &font_table[1];
    }
    else if (c == '(') {
        t = &font_table[2];
    }
    else if (c == ')') {
        t = &font_table[3];
    }
    else if (c == ',') {
        t = &font_table[4];
    }
    else if (c == '.') {
        t = &font_table[5];
    }
    else if(c >= '0' && c<= '9')
    {
        t = &font_table[(c-'0' + 6)];
    }
    else if(c == ':')
    {
        t = &font_table[16];
    }
    else if(c == '?')
    {
        t = &font_table[17];
    }
    else if(c >= 'A' && c <= 'Z')
    {
        t = &font_table[((c-'A')+18)];
    }
    else if(c >= 'a' && c <= 'z')
    {
        t = &font_table[((c-'a')+18+26)];
    }
    else
    {
        return false;
    }
    // EDIT END ---

//    xil_printf("looking up %c, margin:%d\r\n", c, t->margin_top);

    sym->width = t->width;
    sym->height = t->height;
    sym->margin_top = t->margin_top;
    sym->margin_bottom = t->margin_bottom;
    sym->margin_left = t->margin_left;
    sym->margin_right = t->margin_right;
    sym->bmp_index = t->bmp_index;

    return true;
}

static bool omori24_lookup(utf8_t c, font_symbol_t *sym)
{
    return omori_lookup(c, sym, omori24_table);
}

static bool omori64_lookup(utf8_t c, font_symbol_t *sym)
{
    return omori_lookup(c, sym, omori64_table);
}

